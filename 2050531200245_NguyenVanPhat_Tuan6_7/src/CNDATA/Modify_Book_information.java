    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(this, "Do you want to update?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (ret != JOptionPane.YES_OPTION) {
            return;
        }

        String update = "update books set Tiltle = ?, price = ? where id = ?";
        System.out.println(update);

        PreparedStatement ps = null;
        ResultSet rs = null;
        Connection connection = ConnectionJDBC.getJDBCConnection();
        try {

            ps = connection.prepareStatement(update);

            ps.setString(1, txtTitle.getText());
            ps.setFloat(2, Float.parseFloat(txtPrice.getText()));
            ps.setInt(3, id); //id được chọn từ combobox

            ret = ps.executeUpdate();
            if (ret != -1) {
                lblStatus.setText("The book has been updated");

            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (connection != null) {
                    connection.close();
                }

                if (rs != null) {
                    rs.close();
                }

                if (ps != null) {
                    ps.close();
                }
            } catch (Exception ex2) {
                ex2.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void cmbConditionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbConditionActionPerformed
        // TODO add your handling code here:

        PreparedStatement ps = null;
        ResultSet rs = null;
        Connection connection = ConnectionJDBC.getJDBCConnection();
        try {

            id = Integer.parseInt((String) cmbCondition.getSelectedItem());
            String sql = "Select * from books where id = ?";

            ps = connection.prepareStatement(sql);
            ps.setInt(1, id);

            rs = ps.executeQuery();
            if (rs.next()) {
                //Hiển thị dữ liệu vào các textfield
                txtTitle.setText(rs.getString("Tiltle"));
                txtPrice.setText(rs.getString("Price"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (connection != null) {
                    connection.close();
                }

                if (rs != null) {
                    rs.close();
                }

                if (ps != null) {
                    ps.close();
                }
            } catch (Exception ex2) {
                ex2.printStackTrace();
            }
        }


    }//GEN-LAST:event_cmbConditionActionPerformed

