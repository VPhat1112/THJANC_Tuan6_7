    private void init() {

        Statement st = null;
        ResultSet rs = null;
        Connection connection = ConnectionJDBC.getJDBCConnection();
        try {

            st = connection.createStatement();
            rs = st.executeQuery("select id from books ");
            Vector data = new Vector();

            while (rs.next()) {
                data.add(rs.getString("id"));
            }

            cmbModel = new DefaultComboBoxModel(data);
            cmbCondition.setModel(cmbModel);

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (connection != null) {
                    connection.close();
                }
                if (st != null) {
                    st.close();
                }
                if (rs != null) {
                    rs.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }

